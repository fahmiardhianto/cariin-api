class Product
  attr_accessor :name, :item_url, :price, :location, :shop, :image_url

  def set_name(name)
    self.name = name
  end

  def set_price(price)
    self.price = price
  end

  def set_location(location)
    self.location = location
  end

  def set_url(url)
    self.item_url = url
  end

  def set_shop(shop)
    self.shop = shop
  end

  def set_shop(shop)
    self.shop = shop
  end

  def set_image_url(url)
    self.image_url = url
  end
end
